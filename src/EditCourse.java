    public EditCourse() {
        initComponents();
        this.setResizable(false);
        this.setLocationRelativeTo(null);
        CourseQuery co = new CourseQuery();  
        ArrayList<Course> cList = co.courseDisplay(Current_CourseID);
        this.jTextBoxcourseid.setText(cList.get(0).getCourse_id()); 
        this.jTextBoxcoursename.setText(cList.get(0).getCourse_name());
        
        fillCombo();
        
        int a=0;
        for(int i=0;i < colList.size(); i++)
        {      
            System.out.println(colList.get(i).getCollege_id());
                
            if(colList.get(i).getCollege_id().equals(cList.get(0).getCollege_id()) )
            {
                a = i; 
                break;
            }
        } 
        a++; 
        jComboBox1.setSelectedIndex(a);
    }
    private void fillCombo()
    {        
        jComboBox1.addItem("Please choose...");
        for(int i=0;i < colList.size(); i++)
        {
            jComboBox1.addItem(colList.get(i).getCollege_name());
        }
    }
    

