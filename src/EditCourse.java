    private void jButtoneditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtoneditActionPerformed
         if(!jTextBoxcourseid.getText().isEmpty() && !jTextBoxcoursename.getText().isEmpty()) {
            String courseID = jTextBoxcourseid.getText().toUpperCase();
            String courseName = jTextBoxcoursename.getText();
            String collegeID = null;
           
            if(selectedindex==0){
                JOptionPane.showMessageDialog(null, "Please Choose a College");
            }
            else
            {
                selectedindex--; 
                                   
                collegeID = colList.get(selectedindex).getCollege_id(); 
                
                System.out.println(collegeID);
                try{ 
                    Course c = new Course(courseID, courseName, collegeID, null);
                    CourseQuery cq = new CourseQuery();                    
                    cq.editCourse(c, Current_CourseID);

                    MasterCourse mc = new MasterCourse();
                    mc.setVisible(true);
                    mc.pack();
                    mc.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
                    this.dispose();
                }catch(Exception ex){
                    Logger.getLogger(EditCourse.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
        else {
            JOptionPane.showMessageDialog(null, "ERROR!\nOne or many fields are Empty. Try Again...");
        }
    }//GEN-LAST:event_jButtoneditActionPerformed

